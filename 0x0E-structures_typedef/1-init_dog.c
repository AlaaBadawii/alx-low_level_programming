#define NULL (void *)0
/**
 * init_dog - function that initialize a variable of type struct dog.
 * @d: struct dog pointer input
 * @name: char pointer input
 * @age: float input
 * @owner: char pointer input
 */
void init_dog(struct dog *d, char *name, float age, char *owner)
{
	if (d != NULL)
	{
		ptr -> name = name;
		ptr -> age = age;
		ptr -> owner = owner;
	}
}
