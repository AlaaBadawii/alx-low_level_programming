#include "function_pointers"
#include "3-calc"

/**
 * op_add - calc sum of two numbers
 * @a: int input
 * @b: int input
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calc sub of two numbers
 * @a: int input
 * @b: int input
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calc sub of two numbers
 * @a: int input
 * @b: int input
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calc sub of two numbers
 * @a: int input
 * @b: int input
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calc sub of two numbers
 * @a: int input
 * @b: int input
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}

